struct Node                     // Define a Node as Struct
{
    x: i32,
    y: i32,
    g: i32, // Cost from start
    h: i32, // Heuristic Value, Manhattan Distance
}

impl Node 
{
    fn new(x: i32, y: i32, g: i32, h: i32) -> Node 
    {
        Node { x, y, g, h }
    }

    fn f(&self) -> i32      // Calculates the f_score which is the g_score + heuristic value
    {
        self.g + self.h
    }
}

impl Ord for Node           // Defining Orderings for use in the open_set
{
    fn cmp(&self, other: &Self) -> Ordering 
    {
        other.f().cmp(&self.f())
    }
}

impl PartialOrd for Node 
{
    fn partial_cmp(&self, other: &Self) -> Option<Ordering> 
    {
        Some(self.cmp(other))
    }
}

fn astar
(
    start: (i32, i32),
    goal: (i32, i32),
    grid: &[Vec<i32>], // Pass the grid as a reference to a slice of slices
) -> Option<Vec<(i32, i32)>> 
{
    let width = grid[0].len() as i32;
    let height = grid.len() as i32;

    let mut open_set = BinaryHeap::new();
    let mut came_from = HashMap::new();
    let mut g_score = HashMap::new();

    open_set.push(Node::new(start.0, start.1, 0, heuristic(start, goal)));
    g_score.insert(start, 0);

    while !open_set.is_empty() 
    {
        let current = open_set.pop().unwrap();

        if (current.x, current.y) == goal       // Goal found
        {
            return Some(reconstruct_path(came_from, current));
        }

        for neighbor in get_neighbors(current, width, height) 
        {
            let (x, y) = (neighbor.x as usize, neighbor.y as usize);

            // if x < 0 || x >= grid[0].len() || y < 0 || y >= grid.len() || grid[y][x] == 1 {
            //     continue;
            // }
            if x >= grid[0].len() ||  y >= grid.len() || grid[y][x] == 1 
            {
                continue;
            }
            let tentative_g = g_score[&(current.x, current.y)] + 1;

            if !g_score.contains_key(&(neighbor.x, neighbor.y)) || tentative_g < g_score[&(neighbor.x, neighbor.y)] 
            {
                g_score.insert((neighbor.x, neighbor.y), tentative_g);
                let f = tentative_g + heuristic((neighbor.x, neighbor.y), goal);
                open_set.push(Node::new(neighbor.x, neighbor.y, tentative_g, f));
                came_from.insert((neighbor.x, neighbor.y), (current.x, current.y));
            }
        }
    }

    None
}

fn get_neighbors(node: Node, width: i32, height: i32) -> Vec<Node> 
{
    let mut neighbors = Vec::new();
    for dx in -1..=1 
    {
        for dy in -1..=1 
        {
            if dx == 0 && dy == 0 
            {
                continue;
            }
            if dx != 0 && dy != 0 
            {
                continue;
            }
            let new_x = node.x + dx;
            let new_y = node.y + dy;
            if new_x >= 0 && new_x < width && new_y >= 0 && new_y < height 
            {
                neighbors.push(Node::new(new_x, new_y, 0, 0));
            }
        }
    }
    neighbors
}

fn heuristic(start: (i32, i32), goal: (i32, i32)) -> i32 
{
    // Manhattan distance
    (goal.0 - start.0).abs() + (goal.1 - start.1).abs()
}

fn reconstruct_path(came_from: HashMap<(i32, i32), (i32, i32)>, current: Node) -> Vec<(i32, i32)> 
{
    let mut path = vec![(current.x, current.y)];
    let mut node = current;

    while let Some(&prev) = came_from.get(&(node.x, node.y)) 
    {
        path.push(prev);
        node = Node::new(prev.0, prev.1, 0, 0);
    }

    path.reverse();
    path
}

