def astar(grid, start, goal):
    startt = time.time()
    for_time = 0
    while_time = 0
    rows, cols = len(grid), len(grid[0])
    queue = [(0, start)]
    came_from = {}
    gscore = {(row, col): float('inf') for row in range(rows) for col in range(cols)}
    gscore[start] = 0
    fscore = {(row, col): float('inf') for row in range(rows) for col in range(cols)}
    fscore[start] = Remaining(start, goal)

    while queue:
        start_while = time.time()
        current_f, current_position = heapq.heappop(queue)
 
        if current_position == goal:
            print("Total time taken by all get_neighbors calls: {:.6f}".format(get_neighbors_time))
            print("Time taken by for loop in A* is: ", for_time)
            print("Time taken by while loop in A* is: ", while_time)
            print("Time taken by A* is: ", time.time() - startt)
            return reconstruct_path(came_from, current_position)
        start_t = time.time() 
        for neighbor in get_neighbors(current_position, rows, cols, grid):
            variable_gscore = gscore[current_position] + distance(current_position, neighbor)
 
            if variable_gscore < gscore[neighbor]:
                came_from[neighbor] = current_position
                gscore[neighbor] = variable_gscore
                fscore[neighbor] = gscore[neighbor] + Remaining(neighbor, goal)
                heapq.heappush(queue, (fscore[neighbor], neighbor))
        for_time += time.time() - start_t
        while_time += time.time() - start_while    
 
 
def Remaining(position, goal):
    return abs(position[0] - goal[0]) + abs(position[1] - goal[1])
 
def distance(position1, position2):
    return math.sqrt((position1[0] - position2[0])**2 + (position1[1] - position2[1])**2)

def get_neighbors(position, rows, cols, grid):
    global get_neighbors_time
    start = time.time()
    row, col = position
    neighbors = []
 
# Checking Corners with any obstacle
    for dr in [-1, 0, 1]:
        for dc in [-1, 0, 1]:
            if dr == 0 and dc == 0:
                continue
            if dr != 0 and dc != 0:
                continue
            new_row, new_col = row + dr, col + dc
            if 0 <= new_row < rows and 0 <= new_col < cols and grid[new_row][new_col] == 0:
                if dr != 0 and dc != 0:  # Check for diagonal movement
                    if grid[row + dr][col] == 1 or grid[row][col + dc] == 1:
                        if grid[row + dr][col] == 1 and grid[row][col + dc] == 1:
                            continue
                neighbors.append((new_row, new_col))
    get_neighbors_time += time.time() - start
    return neighbors
 
def reconstruct_path(came_from, current):
    start_time = time.time()
    path = [current]
    while current in came_from:
        current = came_from[current]
        path.insert(0, current)
    print("Time taken by reconstruct_path is: {:.6f}".format(time.time() - start_time))
    return path
 
 
